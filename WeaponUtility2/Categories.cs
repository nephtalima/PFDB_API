namespace PFDB.WeaponUtility;


/// <summary>
/// Defines an enumerator for different categories in Phantom Forces.
/// </summary>
public enum Categories
{
	/// <summary>
	/// Represents the assault rifle category.
	/// </summary>
	AssaultRifles,

	/// <summary>
	/// Represents the personal defenese weapon (PDW) category.
	/// </summary>
	PersonalDefenseWeapons,

	/// <summary>
	/// Represents the light machine gun (LMG) category.
	/// </summary>
	LightMachineGuns,

	/// <summary>
	/// Represents the sniper rifle category.
	/// </summary>
	SniperRifles,

	/// <summary>
	/// Represents the battle rifle category.
	/// </summary>
	BattleRifles,

	/// <summary>
	/// Represents the carbine category.
	/// </summary>
	Carbines,

	/// <summary>
	/// Represents the designated marksman rifle (DMR) category.
	/// </summary>
	DesignatedMarksmanRifles,

	/// <summary>
	/// Represents the shotgun category.
	/// </summary>
	Shotguns,

	/// <summary>
	/// Represents the pistol category.
	/// </summary>
	Pistols,

	/// <summary>
	/// Represents the machine pistol category.
	/// </summary>
	MachinePistols,

	/// <summary>
	/// Represents the revolver category.
	/// </summary>
	Revolvers,

	/// <summary>
	/// Represents the other/miscellaneous category.
	/// </summary>
	Other,

	/// <summary>
	/// Represents the fragmentation grenade category.
	/// </summary>
	FragmentationGrenades,

	/// <summary>
	/// Represents the high-explosive grenade category.
	/// </summary>
	HighExplosiveGrenades,

	/// <summary>
	/// Represents the impact grenade category.
	/// </summary>
	ImpactGrenades,

	/// <summary>
	/// Represents the one-handed blade melee category.
	/// </summary>
	OneHandBladeMelees,

	/// <summary>
	/// Represents the two-handed blade melee category.
	/// </summary>
	TwoHandBladeMelees,

	/// <summary>
	/// Represents the one-handed blunt melee category.
	/// </summary>
	OneHandBluntMelees,

	/// <summary>
	/// Represents the two-handed blunt melee category.
	/// </summary>
	TwoHandBluntMelees
}
