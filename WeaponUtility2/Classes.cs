namespace PFDB.WeaponUtility;


/// <summary>
/// Defines an enumerator for categorizing classes.
/// </summary>
public enum Classes
{
	/// <summary>
	/// Assault class. Contains assault rifles, battle rifles, carbines and shotguns categories.
	/// </summary>
	Assault,
	/// <summary>
	/// Scout class. Contains personal defense weapons, carbines, designated marksman rifles, and shotguns categories.
	/// </summary>
	Scout,
	/// <summary>
	/// Support class. Contains light machine guns, battle rifles, carbines, and shotguns categories.
	/// </summary>
	Support,
	/// <summary>
	/// Recon class. Contains sniper rifles, battle rifles, carbines, and designated marksman rifles categories.
	/// </summary>
	Recon
}
